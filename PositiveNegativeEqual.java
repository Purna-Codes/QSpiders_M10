class PositiveNegativeEqual
{
	public static void main(String[] args) 
	{
		int n=-30;
		if(n!=0)
		{
			if (n>0)
			{
				System.out.println(n+" is positive");
			}
			else
			{
				System.out.println(n+" is negative");
			}
		}
		else
		{
			System.out.println(n+" n is equal to 0");
		}	  
	}
}
