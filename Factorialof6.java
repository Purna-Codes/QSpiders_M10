class Factorialof6
{
	public static void main(String[] args) 
	{
		int n=6,prod=1;
		for (int i=1;i<=n ;i++ )
		{
			prod=prod*i;
		}
		System.out.print(prod);
		System.out.println();
		
		/*Print method must have some argument to be excuted
		  where as "println" need not an argument to be executed*/
	}
}
